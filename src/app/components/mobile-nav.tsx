export function MobileNav() {
  const pathname = usePathname();
  const lastScrollRef = useRef(0);
  const [loggedIn, setLoggedIn] = useState(false);

  // Check auth state
  useEffect(() => {
    setLoggedIn(!!getLocalUser());
    function onStorage() {
      setLoggedIn(!!getLocalUser());
    }
    window.addEventListener("storage", onStorage);
    return () => window.removeEventListener("storage", onStorage);
  }, []);

  // Hide top nav on scroll down, show on scroll up (uses ref to avoid re-registering listener)
  useEffect(() => {
    let ticking = false;
    function onScroll() {
      if (ticking) return;
      ticking = true;
      requestAnimationFrame(() => {
        const currentScroll = window.scrollY;
        const nav = document.querySelector(".nav-auto-hide");
        if (nav) {
          if (currentScroll > lastScrollRef.current && currentScroll > 80) {
            nav.classList.add("nav-hidden");
          } else {
            nav.classList.remove("nav-hidden");
          }
        }
        lastScrollRef.current = currentScroll;
        ticking = false;
      });
    }

    window.addEventListener("scroll", onScroll, { passive: true });
    return () => window.removeEventListener("scroll", onScroll);
  }, []); // stable â€” uses ref, no deps needed

  const NAV_ITEMS = loggedIn ? NAV_ITEMS_AUTHED : NAV_ITEMS_GUEST;

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 border-t border-black/[0.07] bg-[#f8f7f4]/95 backdrop-blur-xl md:hidden dark:border-white/[0.06] dark:bg-[#0f0f0f]/95"
      style={{ paddingBottom: "env(safe-area-inset-bottom, 0)" }}
    >
      <div className="flex items-center justify-around px-2 py-1">
        {NAV_ITEMS.map((item) => {
          const active = pathname === item.href;
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex min-h-[44px] min-w-[44px] flex-col items-center justify-center gap-0.5 rounded-lg px-3 py-1.5 text-[10px] font-medium transition-colors ${
                active
                  ? "text-[#1a3a5c] dark:text-[#c8922a]"
                  : "text-zinc-400 hover:text-zinc-600 dark:text-zinc-500 dark:hover:text-zinc-300"
              }`}
            >
              {item.icon}
              <span>{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
